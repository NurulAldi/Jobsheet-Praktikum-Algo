#include<stdio.h>

int main(){
	// pp = panjang persegi panjang
	double pp = 10;
	// lp = lebar persegi panjang
	double lp = 5;
	// lpp = luas persegi panjang
	float lpp;
	
	// menghitung luas persegi panjang
	lpp = pp*lp;
	// menampilkan luas persegi panjang
	printf("Luas persegi panjang adalah : %.2f meter", lpp);
	
	return 0;
	
}
