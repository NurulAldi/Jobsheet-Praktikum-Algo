#include<stdio.h>

int main(){
	// pas = panjang alas segitiga
	double pas = 8;
	// ts = tinggi segitiga
	double ts = 5;
	// ls = luas segitiga
	float ls;
	
	// menghitung luas segitiga
	ls = (pas*ts)/2;
	// menampilkan luas segitiga
	printf("Luas segitiga adalah : %.2f meter", ls);
	
	return 0;
	
}
